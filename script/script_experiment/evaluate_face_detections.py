import argparse
import csv
import json
import sys
from pathlib import Path
from typing import Any


TARGET_REGIONS = {"head", "face_bbox"}
MATCH_POLICIES = {"iou", "iou_or_coverage"}


def parse_args() -> argparse.Namespace:
    parser = argparse.ArgumentParser(
        description=(
            "Evaluate face detections against CVAT ground truth. "
            "Head boxes are the default primary target for mosaic coverage."
        )
    )
    parser.add_argument(
        "--ground-truth",
        type=Path,
        default=Path("experiments/scrfd_mediapipe_baseline/reports/ground_truth.json"),
        help="Ground truth JSON generated by convert_cvat_annotations.py.",
    )
    parser.add_argument(
        "--predictions",
        type=Path,
        required=True,
        help="Prediction JSON with records[].detections[].bbox.",
    )
    parser.add_argument(
        "--target-region",
        choices=sorted(TARGET_REGIONS),
        default="head",
        help="Ground truth region to evaluate. Default: head.",
    )
    parser.add_argument(
        "--iou-threshold",
        type=float,
        default=0.5,
        help="Minimum IoU for a prediction to match a ground truth region. Default: 0.5.",
    )
    parser.add_argument(
        "--match-policy",
        choices=sorted(MATCH_POLICIES),
        default="iou",
        help=(
            "Matching rule. 'iou' uses IoU only. 'iou_or_coverage' matches when either "
            "IoU or ground-truth coverage reaches its threshold."
        ),
    )
    parser.add_argument(
        "--coverage-threshold",
        type=float,
        default=0.5,
        help=(
            "Minimum ground-truth coverage for --match-policy iou_or_coverage. "
            "Coverage is intersection area / ground-truth area. Default: 0.5."
        ),
    )
    parser.add_argument(
        "--bbox-scale",
        type=float,
        default=1.0,
        help="Scale prediction boxes around their center before matching. Default: 1.0.",
    )
    parser.add_argument(
        "--comparison-scope",
        choices=["all", "paired_modes"],
        default="all",
        help=(
            "Use all annotation records, or only images that have both simple and skeleton "
            "annotations for fair mode comparison. Default: all."
        ),
    )
    parser.add_argument(
        "--summary-output",
        type=Path,
        default=Path("experiments/scrfd_mediapipe_baseline/reports/detection_summary.json"),
        help="Path to write evaluation summary JSON.",
    )
    parser.add_argument(
        "--matches-output",
        type=Path,
        default=Path("experiments/scrfd_mediapipe_baseline/reports/detection_matches.csv"),
        help="Path to write per-image match details CSV.",
    )
    return parser.parse_args()


def load_json(path: Path) -> dict[str, Any]:
    with path.open(encoding="utf-8") as file:
        return json.load(file)


def normalize_key(record: dict[str, Any]) -> tuple[str, str, str]:
    return (
        record.get("annotation_kind", ""),
        record.get("image_stem", ""),
        record.get("image_name", ""),
    )


def bbox_area(bbox: dict[str, float]) -> float:
    width = max(0.0, float(bbox["xbr"]) - float(bbox["xtl"]))
    height = max(0.0, float(bbox["ybr"]) - float(bbox["ytl"]))
    return width * height


def bbox_iou(first: dict[str, float], second: dict[str, float]) -> float:
    xtl = max(float(first["xtl"]), float(second["xtl"]))
    ytl = max(float(first["ytl"]), float(second["ytl"]))
    xbr = min(float(first["xbr"]), float(second["xbr"]))
    ybr = min(float(first["ybr"]), float(second["ybr"]))
    intersection = bbox_area({"xtl": xtl, "ytl": ytl, "xbr": xbr, "ybr": ybr})
    union = bbox_area(first) + bbox_area(second) - intersection
    if union <= 0.0:
        return 0.0
    return intersection / union


def bbox_intersection_area(first: dict[str, float], second: dict[str, float]) -> float:
    xtl = max(float(first["xtl"]), float(second["xtl"]))
    ytl = max(float(first["ytl"]), float(second["ytl"]))
    xbr = min(float(first["xbr"]), float(second["xbr"]))
    ybr = min(float(first["ybr"]), float(second["ybr"]))
    return bbox_area({"xtl": xtl, "ytl": ytl, "xbr": xbr, "ybr": ybr})


def bbox_gt_coverage(ground_truth: dict[str, float], prediction: dict[str, float]) -> float:
    area = bbox_area(ground_truth)
    if area <= 0.0:
        return 0.0
    return bbox_intersection_area(ground_truth, prediction) / area


def select_ground_truth_regions(record: dict[str, Any], target_region: str) -> list[dict[str, Any]]:
    if target_region == "head":
        return record.get("heads", [])
    if target_region == "face_bbox":
        return record.get("faces", [])
    raise ValueError(f"Unsupported target region: {target_region}")


def prediction_records_by_key(predictions: dict[str, Any]) -> dict[tuple[str, str, str], dict[str, Any]]:
    records = predictions.get("records", [])
    by_key = {}
    for record in records:
        key = normalize_key(record)
        if key == ("", "", ""):
            key = ("", record.get("image_stem", ""), record.get("image_name", ""))
        by_key[key] = record
    return by_key


def find_prediction(
    predictions_by_key: dict[tuple[str, str, str], dict[str, Any]],
    ground_truth_record: dict[str, Any],
) -> dict[str, Any] | None:
    exact_key = normalize_key(ground_truth_record)
    if exact_key in predictions_by_key:
        return predictions_by_key[exact_key]

    fallback_key = ("", ground_truth_record.get("image_stem", ""), ground_truth_record.get("image_name", ""))
    return predictions_by_key.get(fallback_key)


def greedy_match(
    ground_truth_regions: list[dict[str, Any]],
    detections: list[dict[str, Any]],
    iou_threshold: float,
    bbox_scale: float,
    match_policy: str = "iou",
    coverage_threshold: float = 0.5,
    image_width: int | None = None,
    image_height: int | None = None,
) -> tuple[list[dict[str, Any]], int, int, int]:
    candidates = []
    for gt_index, gt_region in enumerate(ground_truth_regions):
        gt_bbox = gt_region["bbox"]
        for pred_index, detection in enumerate(detections):
            pred_bbox = scale_bbox(
                detection["bbox"],
                bbox_scale,
                image_width=image_width,
                image_height=image_height,
            )
            iou = bbox_iou(gt_bbox, pred_bbox)
            coverage = bbox_gt_coverage(gt_bbox, pred_bbox)
            matched = iou >= iou_threshold
            if match_policy == "iou_or_coverage":
                matched = matched or coverage >= coverage_threshold
            if matched:
                candidates.append((max(iou, coverage), iou, coverage, gt_index, pred_index))

    candidates.sort(reverse=True)
    matched_gt = set()
    matched_pred = set()
    matches = []

    for _, iou, coverage, gt_index, pred_index in candidates:
        if gt_index in matched_gt or pred_index in matched_pred:
            continue
        matched_gt.add(gt_index)
        matched_pred.add(pred_index)
        matches.append(
            {
                "ground_truth_index": gt_index,
                "prediction_index": pred_index,
                "iou": iou,
                "gt_coverage": coverage,
            }
        )

    true_positive = len(matches)
    false_negative = len(ground_truth_regions) - true_positive
    false_positive = len(detections) - true_positive
    return matches, true_positive, false_positive, false_negative


def scale_bbox(
    bbox: dict[str, float],
    scale: float,
    image_width: int | None = None,
    image_height: int | None = None,
) -> dict[str, float]:
    if scale <= 0:
        raise ValueError("--bbox-scale must be greater than 0.")
    center_x = (float(bbox["xtl"]) + float(bbox["xbr"])) / 2.0
    center_y = (float(bbox["ytl"]) + float(bbox["ybr"])) / 2.0
    width = (float(bbox["xbr"]) - float(bbox["xtl"])) * scale
    height = (float(bbox["ybr"]) - float(bbox["ytl"])) * scale
    xtl = center_x - width / 2.0
    ytl = center_y - height / 2.0
    xbr = center_x + width / 2.0
    ybr = center_y + height / 2.0

    if image_width is not None:
        xtl = max(0.0, min(float(image_width), xtl))
        xbr = max(0.0, min(float(image_width), xbr))
    if image_height is not None:
        ytl = max(0.0, min(float(image_height), ytl))
        ybr = max(0.0, min(float(image_height), ybr))

    return {
        "xtl": xtl,
        "ytl": ytl,
        "xbr": xbr,
        "ybr": ybr,
        "width": max(0.0, xbr - xtl),
        "height": max(0.0, ybr - ytl),
    }


def evaluate(
    ground_truth: dict[str, Any],
    predictions: dict[str, Any],
    target_region: str,
    iou_threshold: float,
    comparison_scope: str,
    bbox_scale: float,
    match_policy: str,
    coverage_threshold: float,
) -> tuple[dict[str, Any], list[dict[str, Any]]]:
    predictions_by_key = prediction_records_by_key(predictions)
    details = []
    totals = empty_totals()
    by_annotation_mode: dict[str, dict[str, Any]] = {}
    records = merge_records_by_image(select_records_for_scope(ground_truth.get("records", []), comparison_scope))

    for record in records:
        annotation_mode = record.get("annotation_mode", "unknown")
        if annotation_mode not in by_annotation_mode:
            by_annotation_mode[annotation_mode] = empty_totals()

        ground_truth_regions = select_ground_truth_regions(record, target_region)
        prediction = find_prediction(predictions_by_key, record)
        detections = [] if prediction is None else prediction.get("detections", [])
        matches, tp, fp, fn = greedy_match(
            ground_truth_regions,
            detections,
            iou_threshold,
            bbox_scale,
            match_policy=match_policy,
            coverage_threshold=coverage_threshold,
            image_width=record.get("width"),
            image_height=record.get("height"),
        )
        matched_iou_sum = sum(match["iou"] for match in matches)

        update_totals(totals, len(ground_truth_regions), len(detections), tp, fp, fn, matched_iou_sum)
        update_totals(
            by_annotation_mode[annotation_mode],
            len(ground_truth_regions),
            len(detections),
            tp,
            fp,
            fn,
            matched_iou_sum,
        )

        details.append(
            {
                "annotation_kind": record.get("annotation_kind", ""),
                "annotation_mode": annotation_mode,
                "image_stem": record.get("image_stem", ""),
                "image_name": record.get("image_name", ""),
                "target_region": target_region,
                "ground_truth_count": len(ground_truth_regions),
                "detection_count": len(detections),
                "true_positive": tp,
                "false_positive": fp,
                "false_negative": fn,
                "mean_iou": matched_iou_sum / tp if tp else 0.0,
                "matched_pairs": json.dumps(matches, ensure_ascii=False),
            }
        )

    summary = build_summary(
        totals=totals,
        target_region=target_region,
        iou_threshold=iou_threshold,
        primary_target_region=ground_truth.get("evaluation_policy", {}).get(
            "primary_target_region", "head"
        ),
        comparison_scope=comparison_scope,
        bbox_scale=bbox_scale,
    )
    summary["match_policy"] = match_policy
    summary["coverage_threshold"] = coverage_threshold
    summary["by_annotation_mode"] = {
        mode: build_summary(
            totals=mode_totals,
            target_region=target_region,
            iou_threshold=iou_threshold,
            primary_target_region=summary["primary_target_region"],
            comparison_scope=comparison_scope,
            bbox_scale=bbox_scale,
        )
        for mode, mode_totals in sorted(by_annotation_mode.items())
    }
    for mode_summary in summary["by_annotation_mode"].values():
        mode_summary["match_policy"] = match_policy
        mode_summary["coverage_threshold"] = coverage_threshold
    add_frame_macro_metrics(summary, details)
    return summary, details


def add_frame_macro_metrics(summary: dict[str, Any], details: list[dict[str, Any]]) -> None:
    def precision(row: dict[str, Any]) -> float:
        tp = row["true_positive"]
        fp = row["false_positive"]
        return tp / (tp + fp) if tp + fp else 0.0

    def recall(row: dict[str, Any]) -> float:
        tp = row["true_positive"]
        fn = row["false_negative"]
        return tp / (tp + fn) if tp + fn else 0.0

    def mean(values: list[float]) -> float:
        return sum(values) / len(values) if values else 0.0

    summary["frame_macro_precision"] = mean([precision(row) for row in details])
    summary["frame_macro_recall"] = mean([recall(row) for row in details])
    summary["frames_with_false_positive"] = sum(1 for row in details if row["false_positive"] > 0)
    summary["frames_with_false_negative"] = sum(1 for row in details if row["false_negative"] > 0)

    by_mode: dict[str, list[dict[str, Any]]] = {}
    for row in details:
        by_mode.setdefault(row["annotation_mode"], []).append(row)
    for mode, rows in by_mode.items():
        mode_summary = summary["by_annotation_mode"].get(mode)
        if mode_summary is None:
            continue
        mode_summary["frame_macro_precision"] = mean([precision(row) for row in rows])
        mode_summary["frame_macro_recall"] = mean([recall(row) for row in rows])
        mode_summary["frames_with_false_positive"] = sum(1 for row in rows if row["false_positive"] > 0)
        mode_summary["frames_with_false_negative"] = sum(1 for row in rows if row["false_negative"] > 0)


def merge_records_by_image(records: list[dict[str, Any]]) -> list[dict[str, Any]]:
    merged: dict[tuple[str, str, str, str], dict[str, Any]] = {}
    for record in records:
        key = (
            record.get("annotation_kind", ""),
            record.get("annotation_mode", ""),
            record.get("image_stem", ""),
            record.get("image_name", ""),
        )
        if key not in merged:
            merged[key] = {
                **record,
                "heads": list(record.get("heads", [])),
                "faces": list(record.get("faces", [])),
                "simple_landmarks": list(record.get("simple_landmarks", [])),
                "skeleton_landmarks": list(record.get("skeleton_landmarks", [])),
            }
            continue

        merged[key]["heads"].extend(record.get("heads", []))
        merged[key]["faces"].extend(record.get("faces", []))
        merged[key]["simple_landmarks"].extend(record.get("simple_landmarks", []))
        merged[key]["skeleton_landmarks"].extend(record.get("skeleton_landmarks", []))
    return list(merged.values())


def select_records_for_scope(records: list[dict[str, Any]], comparison_scope: str) -> list[dict[str, Any]]:
    if comparison_scope == "all":
        return records
    if comparison_scope != "paired_modes":
        raise ValueError(f"Unsupported comparison scope: {comparison_scope}")

    modes_by_image: dict[tuple[str, str], set[str]] = {}
    for record in records:
        key = (record.get("image_stem", ""), record.get("image_name", ""))
        modes_by_image.setdefault(key, set()).add(record.get("annotation_mode", "unknown"))

    paired_images = {
        key for key, modes in modes_by_image.items() if {"simple", "skeleton"}.issubset(modes)
    }
    return [
        record
        for record in records
        if (record.get("image_stem", ""), record.get("image_name", "")) in paired_images
    ]


def empty_totals() -> dict[str, Any]:
    return {
        "images": 0,
        "ground_truth_regions": 0,
        "detections": 0,
        "true_positive": 0,
        "false_positive": 0,
        "false_negative": 0,
        "matched_iou_sum": 0.0,
    }


def update_totals(
    totals: dict[str, Any],
    ground_truth_count: int,
    detection_count: int,
    true_positive: int,
    false_positive: int,
    false_negative: int,
    matched_iou_sum: float,
) -> None:
    totals["images"] += 1
    totals["ground_truth_regions"] += ground_truth_count
    totals["detections"] += detection_count
    totals["true_positive"] += true_positive
    totals["false_positive"] += false_positive
    totals["false_negative"] += false_negative
    totals["matched_iou_sum"] += matched_iou_sum


def build_summary(
    totals: dict[str, Any],
    target_region: str,
    iou_threshold: float,
    primary_target_region: str,
    comparison_scope: str,
    bbox_scale: float,
) -> dict[str, Any]:
    tp = totals["true_positive"]
    fp = totals["false_positive"]
    fn = totals["false_negative"]
    precision = tp / (tp + fp) if tp + fp else 0.0
    recall = tp / (tp + fn) if tp + fn else 0.0
    f1 = 2 * precision * recall / (precision + recall) if precision + recall else 0.0
    mean_iou = totals["matched_iou_sum"] / tp if tp else 0.0

    summary = {
        "target_region": target_region,
        "iou_threshold": iou_threshold,
        "comparison_unit": "face",
        "comparison_scope": comparison_scope,
        "bbox_scale": bbox_scale,
        "primary_target_region": primary_target_region,
        "images": totals["images"],
        "ground_truth_regions": totals["ground_truth_regions"],
        "detections": totals["detections"],
        "true_positive": tp,
        "false_positive": fp,
        "false_negative": fn,
        "precision": precision,
        "recall": recall,
        "f1": f1,
        "mean_iou": mean_iou,
    }
    return summary


def write_summary(path: Path, summary: dict[str, Any]) -> None:
    path.parent.mkdir(parents=True, exist_ok=True)
    with path.open("w", encoding="utf-8") as file:
        json.dump(summary, file, ensure_ascii=False, indent=2)
        file.write("\n")


def write_details(path: Path, details: list[dict[str, Any]]) -> None:
    path.parent.mkdir(parents=True, exist_ok=True)
    fieldnames = [
        "annotation_kind",
        "annotation_mode",
        "image_stem",
        "image_name",
        "target_region",
        "ground_truth_count",
        "detection_count",
        "true_positive",
        "false_positive",
        "false_negative",
        "mean_iou",
        "matched_pairs",
    ]
    with path.open("w", newline="", encoding="utf-8") as file:
        writer = csv.DictWriter(file, fieldnames=fieldnames)
        writer.writeheader()
        writer.writerows(details)


def main() -> int:
    args = parse_args()
    if not 0.0 <= args.iou_threshold <= 1.0:
        raise ValueError("--iou-threshold must be between 0 and 1.")
    if not 0.0 <= args.coverage_threshold <= 1.0:
        raise ValueError("--coverage-threshold must be between 0 and 1.")
    if args.bbox_scale <= 0.0:
        raise ValueError("--bbox-scale must be greater than 0.")

    ground_truth = load_json(args.ground_truth)
    predictions = load_json(args.predictions)
    summary, details = evaluate(
        ground_truth=ground_truth,
        predictions=predictions,
        target_region=args.target_region,
        iou_threshold=args.iou_threshold,
        comparison_scope=args.comparison_scope,
        bbox_scale=args.bbox_scale,
        match_policy=args.match_policy,
        coverage_threshold=args.coverage_threshold,
    )
    write_summary(args.summary_output, summary)
    write_details(args.matches_output, details)

    print(f"summary: {args.summary_output}")
    print(f"matches: {args.matches_output}")
    for key in [
        "target_region",
        "bbox_scale",
        "ground_truth_regions",
        "detections",
        "precision",
        "recall",
        "f1",
        "mean_iou",
    ]:
        print(f"{key}: {summary[key]}")
    return 0


if __name__ == "__main__":
    sys.exit(main())
