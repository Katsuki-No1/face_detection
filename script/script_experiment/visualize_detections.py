import argparse
import csv
import json
import random
import sys
from pathlib import Path
from typing import Any

from evaluate_face_detections import bbox_iou, merge_records_by_image, scale_bbox, select_records_for_scope


COLORS = {
    "head_unmatched": (0, 0, 255),
    "head_matched": (0, 180, 0),
    "prediction": (255, 0, 0),
    "prediction_scaled": (0, 200, 255),
    "scrfd_eye": (255, 0, 255),
    "mediapipe_eye": (0, 255, 255),
    "text_bg": (20, 20, 20),
    "text": (255, 255, 255),
}


def parse_args() -> argparse.Namespace:
    parser = argparse.ArgumentParser(
        description="Visualize CVAT Head boxes and SCRFD detections for error inspection."
    )
    parser.add_argument(
        "--ground-truth",
        type=Path,
        default=Path("experiments/scrfd_mediapipe_baseline/reports/ground_truth.json"),
        help="Ground truth JSON generated by convert_cvat_annotations.py.",
    )
    parser.add_argument(
        "--predictions",
        type=Path,
        default=Path("experiments/scrfd_mediapipe_baseline/predictions/predictions.json"),
        help="Prediction JSON generated by run_scrfd_mediapipe.py.",
    )
    parser.add_argument(
        "--output-dir",
        type=Path,
        default=Path("experiments/scrfd_mediapipe_baseline/overlays/head_detection_scale_1.8"),
        help="Directory to write overlay images and index CSV.",
    )
    parser.add_argument(
        "--target-region",
        choices=["head", "face_bbox"],
        default="head",
        help="Ground truth boxes to draw and match. Default: head.",
    )
    parser.add_argument(
        "--bbox-scale",
        type=float,
        default=1.8,
        help="Prediction bbox scale to draw and match. Default: 1.8.",
    )
    parser.add_argument(
        "--iou-threshold",
        type=float,
        default=0.5,
        help="IoU threshold used to classify matches. Default: 0.5.",
    )
    parser.add_argument(
        "--match-policy",
        choices=["iou", "iou_or_coverage"],
        default="iou",
        help="Matching rule for overlay TP/FP/FN labels. Default: iou.",
    )
    parser.add_argument(
        "--coverage-threshold",
        type=float,
        default=0.5,
        help="Ground-truth coverage threshold for --match-policy iou_or_coverage. Default: 0.5.",
    )
    parser.add_argument(
        "--comparison-scope",
        choices=["all", "paired_modes"],
        default="all",
        help="Visualize all records or only simple/skeleton paired images.",
    )
    parser.add_argument(
        "--only-errors",
        action="store_true",
        help="Only write images with at least one false negative or false positive.",
    )
    parser.add_argument(
        "--max-images",
        type=int,
        default=None,
        help="Optional output limit.",
    )
    parser.add_argument(
        "--random-sample",
        type=int,
        default=None,
        help="Randomly sample this many records before visualization.",
    )
    parser.add_argument(
        "--sample-seed",
        type=int,
        default=20260613,
        help="Random seed used with --random-sample. Default: 20260613.",
    )
    parser.add_argument(
        "--show-bbox-values",
        action="store_true",
        help="Draw bbox coordinates and confidence values in overlay labels.",
    )
    return parser.parse_args()


def import_cv2() -> Any:
    try:
        import cv2  # type: ignore
    except Exception as error:
        raise RuntimeError(
            "OpenCV is required. Run with the prepared environment, for example: "
            "MPLCONFIGDIR=/tmp/matplotlib /tmp/fd-env/bin/python "
            "script/script_experiment/visualize_detections.py"
        ) from error
    return cv2


def load_json(path: Path) -> dict[str, Any]:
    with path.open(encoding="utf-8") as file:
        return json.load(file)


def prediction_index(predictions: dict[str, Any]) -> dict[tuple[str, str, str], dict[str, Any]]:
    indexed = {}
    for record in predictions.get("records", []):
        key = (
            record.get("annotation_kind", ""),
            record.get("image_stem", ""),
            record.get("image_name", ""),
        )
        indexed[key] = record
    return indexed


def draw_box(
    image: Any,
    bbox: dict[str, float],
    color: tuple[int, int, int],
    cv2: Any,
    thickness: int = 2,
) -> None:
    cv2.rectangle(
        image,
        (int(round(bbox["xtl"])), int(round(bbox["ytl"]))),
        (int(round(bbox["xbr"])), int(round(bbox["ybr"]))),
        color,
        thickness,
    )


def draw_label(
    image: Any,
    text: str,
    x: int,
    y: int,
    cv2: Any,
    scale: float = 0.55,
) -> None:
    font = cv2.FONT_HERSHEY_SIMPLEX
    thickness = 1
    (width, height), baseline = cv2.getTextSize(text, font, scale, thickness)
    x = max(0, x)
    y = max(height + baseline + 2, y)
    cv2.rectangle(
        image,
        (x, y - height - baseline - 4),
        (x + width + 6, y + 2),
        COLORS["text_bg"],
        -1,
    )
    cv2.putText(image, text, (x + 3, y - baseline - 1), font, scale, COLORS["text"], thickness)


def draw_points(
    image: Any,
    points: list[dict[str, float]],
    color: tuple[int, int, int],
    cv2: Any,
    prefix: str | None = None,
) -> None:
    for point in points:
        x = int(round(point["x"]))
        y = int(round(point["y"]))
        cv2.circle(image, (x, y), 4, color, -1)
        if prefix is not None:
            label = point.get("label", "")
            draw_label(image, f"{prefix}:{label}", x + 5, y - 5, cv2, scale=0.42)


def eye_points(points: list[dict[str, float]]) -> list[dict[str, float]]:
    return [point for point in points if point.get("label") in {"right_eye", "left_eye"}]


def bbox_text(bbox: dict[str, float]) -> str:
    return (
        f"({bbox['xtl']:.0f},{bbox['ytl']:.0f})-"
        f"({bbox['xbr']:.0f},{bbox['ybr']:.0f})"
    )


def match_for_visualization(
    heads: list[dict[str, Any]],
    detections: list[dict[str, Any]],
    bbox_scale: float,
    iou_threshold: float,
    match_policy: str,
    coverage_threshold: float,
    width: int,
    height: int,
) -> tuple[list[dict[str, Any]], set[int], set[int]]:
    candidates = []
    for head_index, head in enumerate(heads):
        for detection_index, detection in enumerate(detections):
            scaled = scale_bbox(detection["bbox"], bbox_scale, width, height)
            iou = bbox_iou(head["bbox"], scaled)
            head_area = max(0.0, head["bbox"]["width"]) * max(0.0, head["bbox"]["height"])
            x1 = max(head["bbox"]["xtl"], scaled["xtl"])
            y1 = max(head["bbox"]["ytl"], scaled["ytl"])
            x2 = min(head["bbox"]["xbr"], scaled["xbr"])
            y2 = min(head["bbox"]["ybr"], scaled["ybr"])
            intersection = max(0.0, x2 - x1) * max(0.0, y2 - y1)
            coverage = intersection / head_area if head_area else 0.0
            matched = iou >= iou_threshold
            if match_policy == "iou_or_coverage":
                matched = matched or coverage >= coverage_threshold
            if matched:
                candidates.append((max(iou, coverage), iou, coverage, head_index, detection_index))

    candidates.sort(reverse=True)
    matched_heads = set()
    matched_detections = set()
    matches = []
    for _, iou, coverage, head_index, detection_index in candidates:
        if head_index in matched_heads or detection_index in matched_detections:
            continue
        matched_heads.add(head_index)
        matched_detections.add(detection_index)
        matches.append(
            {
                "ground_truth_index": head_index,
                "prediction_index": detection_index,
                "iou": iou,
                "gt_coverage": coverage,
            }
        )
    return matches, matched_heads, matched_detections


def safe_name(record: dict[str, Any]) -> str:
    stem = record["image_stem"].replace("/", "_")
    name = Path(record["image_name"]).stem
    mode = record.get("annotation_mode", "unknown")
    return f"{mode}__{stem}__{name}.jpg"


def visualize_record(
    record: dict[str, Any],
    prediction: dict[str, Any] | None,
    args: argparse.Namespace,
    cv2: Any,
) -> dict[str, Any] | None:
    image = cv2.imread(record["image_path"])
    if image is None:
        return None

    heads = record.get("faces", []) if args.target_region == "face_bbox" else record.get("heads", [])
    detections = [] if prediction is None else prediction.get("detections", [])
    height, width = image.shape[:2]
    matches, matched_heads, matched_detections = match_for_visualization(
        heads,
        detections,
        args.bbox_scale,
        args.iou_threshold,
        args.match_policy,
        args.coverage_threshold,
        width,
        height,
    )
    false_negative = len(heads) - len(matched_heads)
    false_positive = len(detections) - len(matched_detections)
    if args.only_errors and false_negative == 0 and false_positive == 0:
        return None

    for index, head in enumerate(heads):
        color = COLORS["head_matched"] if index in matched_heads else COLORS["head_unmatched"]
        draw_box(image, head["bbox"], color, cv2, thickness=3)
        label = f"{'Face' if args.target_region == 'face_bbox' else 'Head'} {index}"
        if args.show_bbox_values:
            label = f"{label} {bbox_text(head['bbox'])}"
        draw_label(image, label, int(head["bbox"]["xtl"]), int(head["bbox"]["ytl"]) - 4, cv2)

    for index, detection in enumerate(detections):
        scaled = scale_bbox(detection["bbox"], args.bbox_scale, width, height)
        draw_box(image, detection["bbox"], COLORS["prediction"], cv2, thickness=2)
        draw_box(image, scaled, COLORS["prediction_scaled"], cv2, thickness=2)
        score = detection.get("score", 0.0)
        label = f"SCRFD {index} conf={score:.3f}"
        if args.show_bbox_values:
            label = f"{label} raw={bbox_text(detection['bbox'])} scaled={bbox_text(scaled)}"
        draw_label(
            image,
            label,
            int(scaled["xtl"]),
            int(scaled["ybr"]) + 18,
            cv2,
        )
        draw_points(image, eye_points(detection.get("scrfd_landmarks_5pt", [])), COLORS["scrfd_eye"], cv2, "S")
        draw_points(image, detection.get("mediapipe_eye_landmarks", []), COLORS["mediapipe_eye"], cv2, "M")

    header = (
        f"{record['annotation_mode']} | {args.target_region} | scale={args.bbox_scale} | "
        f"TP={len(matches)} FP={false_positive} FN={false_negative}"
    )
    draw_label(image, header, 8, 28, cv2, scale=0.8)

    output_path = args.output_dir / safe_name(record)
    output_path.parent.mkdir(parents=True, exist_ok=True)
    cv2.imwrite(str(output_path), image)

    return {
        "output_path": str(output_path),
        "annotation_kind": record.get("annotation_kind", ""),
        "annotation_mode": record.get("annotation_mode", ""),
        "image_stem": record.get("image_stem", ""),
        "image_name": record.get("image_name", ""),
        "head_count": len(heads),
        "detection_count": len(detections),
        "true_positive": len(matches),
        "false_positive": false_positive,
        "false_negative": false_negative,
        "mean_iou": sum(match["iou"] for match in matches) / len(matches) if matches else 0.0,
        "matched_pairs": json.dumps(matches, ensure_ascii=False),
    }


def write_index(path: Path, rows: list[dict[str, Any]]) -> None:
    path.parent.mkdir(parents=True, exist_ok=True)
    fieldnames = [
        "output_path",
        "annotation_kind",
        "annotation_mode",
        "image_stem",
        "image_name",
        "head_count",
        "detection_count",
        "true_positive",
        "false_positive",
        "false_negative",
        "mean_iou",
        "matched_pairs",
    ]
    with path.open("w", newline="", encoding="utf-8") as file:
        writer = csv.DictWriter(file, fieldnames=fieldnames)
        writer.writeheader()
        writer.writerows(rows)


def main() -> int:
    args = parse_args()
    cv2 = import_cv2()
    ground_truth = load_json(args.ground_truth)
    predictions = load_json(args.predictions)
    predictions_by_key = prediction_index(predictions)
    records = merge_records_by_image(select_records_for_scope(ground_truth.get("records", []), args.comparison_scope))
    if args.random_sample is not None:
        rng = random.Random(args.sample_seed)
        records = rng.sample(records, min(args.random_sample, len(records)))

    rows = []
    for record in records:
        key = (record.get("annotation_kind", ""), record.get("image_stem", ""), record.get("image_name", ""))
        row = visualize_record(record, predictions_by_key.get(key), args, cv2)
        if row is None:
            continue
        rows.append(row)
        if args.max_images is not None and len(rows) >= args.max_images:
            break

    index_path = args.output_dir / "index.csv"
    write_index(index_path, rows)
    print(f"overlays: {args.output_dir}")
    print(f"index: {index_path}")
    print(f"images_written: {len(rows)}")
    return 0


if __name__ == "__main__":
    sys.exit(main())
