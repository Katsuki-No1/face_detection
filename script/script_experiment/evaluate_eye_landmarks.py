import argparse
import csv
import json
import math
import sys
from pathlib import Path
from typing import Any

from evaluate_face_detections import (
    find_prediction,
    greedy_match,
    prediction_records_by_key,
    select_records_for_scope,
)


EYE_LABELS = {"Eye", "e1", "e2", "eye_1", "eye_2", "left_eye", "right_eye"}


def parse_args() -> argparse.Namespace:
    parser = argparse.ArgumentParser(
        description="Evaluate eye landmark accuracy on faces matched by Head boxes."
    )
    parser.add_argument(
        "--ground-truth",
        type=Path,
        default=Path("experiments/scrfd_mediapipe_baseline/reports/ground_truth.json"),
        help="Ground truth JSON generated by convert_cvat_annotations.py.",
    )
    parser.add_argument(
        "--predictions",
        type=Path,
        default=Path("experiments/scrfd_mediapipe_baseline/predictions/predictions.json"),
        help="Prediction JSON generated by run_scrfd_mediapipe.py.",
    )
    parser.add_argument(
        "--iou-threshold",
        type=float,
        default=0.5,
        help="Head IoU threshold used before evaluating eyes. Default: 0.5.",
    )
    parser.add_argument(
        "--comparison-scope",
        choices=["all", "paired_modes"],
        default="paired_modes",
        help="Default paired_modes keeps simple/skeleton comparison on identical images.",
    )
    parser.add_argument(
        "--summary-output",
        type=Path,
        default=Path("experiments/scrfd_mediapipe_baseline/reports/eye_landmark_summary.json"),
        help="Path to write summary JSON.",
    )
    parser.add_argument(
        "--details-output",
        type=Path,
        default=Path("experiments/scrfd_mediapipe_baseline/reports/eye_landmark_details.csv"),
        help="Path to write per-face details CSV.",
    )
    return parser.parse_args()


def load_json(path: Path) -> dict[str, Any]:
    with path.open(encoding="utf-8") as file:
        return json.load(file)


def point_in_bbox(point: dict[str, float], bbox: dict[str, float]) -> bool:
    return (
        bbox["xtl"] <= point["x"] <= bbox["xbr"]
        and bbox["ytl"] <= point["y"] <= bbox["ybr"]
    )


def bbox_diagonal(bbox: dict[str, float]) -> float:
    return math.hypot(bbox["width"], bbox["height"])


def ground_truth_eye_points(record: dict[str, Any], head_bbox: dict[str, float]) -> list[dict[str, float]]:
    points = []
    for shape in record.get("simple_landmarks", []):
        if shape.get("label") not in EYE_LABELS:
            continue
        for point in shape.get("points", []):
            if point_in_bbox(point, head_bbox):
                points.append({"label": shape.get("label", "Eye"), "x": point["x"], "y": point["y"]})

    for skeleton in record.get("skeleton_landmarks", []):
        for point in skeleton.get("points", []):
            if point.get("label") not in EYE_LABELS:
                continue
            if point.get("outside", False):
                continue
            candidate = {"label": point.get("label", "Eye"), "x": point["x"], "y": point["y"]}
            if point_in_bbox(candidate, head_bbox):
                points.append(candidate)
    return points


def predicted_eye_points(detection: dict[str, Any]) -> list[dict[str, float]]:
    points = detection.get("mediapipe_eye_landmarks", [])
    if points:
        return points
    return [
        point
        for point in detection.get("scrfd_landmarks_5pt", [])
        if point.get("label") in {"left_eye", "right_eye"}
    ]


def point_distance(first: dict[str, float], second: dict[str, float]) -> float:
    return math.hypot(first["x"] - second["x"], first["y"] - second["y"])


def match_points(
    ground_truth_points: list[dict[str, float]],
    prediction_points: list[dict[str, float]],
) -> list[dict[str, Any]]:
    candidates = []
    for gt_index, gt_point in enumerate(ground_truth_points):
        for pred_index, pred_point in enumerate(prediction_points):
            candidates.append((point_distance(gt_point, pred_point), gt_index, pred_index))

    candidates.sort()
    matched_gt = set()
    matched_pred = set()
    matches = []
    for distance, gt_index, pred_index in candidates:
        if gt_index in matched_gt or pred_index in matched_pred:
            continue
        matched_gt.add(gt_index)
        matched_pred.add(pred_index)
        matches.append(
            {
                "ground_truth_index": gt_index,
                "prediction_index": pred_index,
                "pixel_error": distance,
            }
        )
    return matches


def empty_totals() -> dict[str, Any]:
    return {
        "images": 0,
        "matched_faces": 0,
        "gt_eye_points": 0,
        "pred_eye_points": 0,
        "matched_eye_points": 0,
        "pixel_error_sum": 0.0,
        "normalized_error_sum": 0.0,
    }


def update_totals(totals: dict[str, Any], detail: dict[str, Any]) -> None:
    totals["matched_faces"] += 1
    totals["gt_eye_points"] += detail["gt_eye_points"]
    totals["pred_eye_points"] += detail["pred_eye_points"]
    totals["matched_eye_points"] += detail["matched_eye_points"]
    totals["pixel_error_sum"] += detail["pixel_error_sum"]
    totals["normalized_error_sum"] += detail["normalized_error_sum"]


def build_summary(totals: dict[str, Any], iou_threshold: float, comparison_scope: str) -> dict[str, Any]:
    matched = totals["matched_eye_points"]
    return {
        "comparison_unit": "eye_landmark_on_matched_head",
        "comparison_scope": comparison_scope,
        "iou_threshold": iou_threshold,
        "images": totals["images"],
        "matched_faces": totals["matched_faces"],
        "gt_eye_points": totals["gt_eye_points"],
        "pred_eye_points": totals["pred_eye_points"],
        "matched_eye_points": matched,
        "eye_point_recall_on_matched_faces": matched / totals["gt_eye_points"]
        if totals["gt_eye_points"]
        else 0.0,
        "mean_pixel_error": totals["pixel_error_sum"] / matched if matched else 0.0,
        "mean_normalized_error": totals["normalized_error_sum"] / matched if matched else 0.0,
    }


def evaluate(
    ground_truth: dict[str, Any],
    predictions: dict[str, Any],
    iou_threshold: float,
    comparison_scope: str,
) -> tuple[dict[str, Any], list[dict[str, Any]]]:
    prediction_index = prediction_records_by_key(predictions)
    records = select_records_for_scope(ground_truth.get("records", []), comparison_scope)
    totals = empty_totals()
    by_mode: dict[str, dict[str, Any]] = {}
    details = []

    for record in records:
        mode = record.get("annotation_mode", "unknown")
        by_mode.setdefault(mode, empty_totals())
        totals["images"] += 1
        by_mode[mode]["images"] += 1

        prediction = find_prediction(prediction_index, record)
        detections = [] if prediction is None else prediction.get("detections", [])
        matches, _, _, _ = greedy_match(
            record.get("heads", []),
            detections,
            iou_threshold,
            bbox_scale=1.0,
            image_width=record.get("width"),
            image_height=record.get("height"),
        )

        for match in matches:
            head = record["heads"][match["ground_truth_index"]]
            detection = detections[match["prediction_index"]]
            gt_points = ground_truth_eye_points(record, head["bbox"])
            pred_points = predicted_eye_points(detection)
            point_matches = match_points(gt_points, pred_points)
            normalizer = max(bbox_diagonal(head["bbox"]), 1.0)
            pixel_error_sum = sum(point_match["pixel_error"] for point_match in point_matches)
            normalized_error_sum = sum(
                point_match["pixel_error"] / normalizer for point_match in point_matches
            )
            detail = {
                "annotation_kind": record.get("annotation_kind", ""),
                "annotation_mode": mode,
                "image_stem": record.get("image_stem", ""),
                "image_name": record.get("image_name", ""),
                "head_index": match["ground_truth_index"],
                "detection_index": match["prediction_index"],
                "head_iou": match["iou"],
                "gt_eye_points": len(gt_points),
                "pred_eye_points": len(pred_points),
                "matched_eye_points": len(point_matches),
                "pixel_error_sum": pixel_error_sum,
                "normalized_error_sum": normalized_error_sum,
                "mean_pixel_error": pixel_error_sum / len(point_matches) if point_matches else 0.0,
                "mean_normalized_error": normalized_error_sum / len(point_matches)
                if point_matches
                else 0.0,
                "point_matches": json.dumps(point_matches, ensure_ascii=False),
            }
            update_totals(totals, detail)
            update_totals(by_mode[mode], detail)
            details.append(detail)

    summary = build_summary(totals, iou_threshold, comparison_scope)
    summary["by_annotation_mode"] = {
        mode: build_summary(mode_totals, iou_threshold, comparison_scope)
        for mode, mode_totals in sorted(by_mode.items())
    }
    return summary, details


def write_summary(path: Path, summary: dict[str, Any]) -> None:
    path.parent.mkdir(parents=True, exist_ok=True)
    with path.open("w", encoding="utf-8") as file:
        json.dump(summary, file, ensure_ascii=False, indent=2)
        file.write("\n")


def write_details(path: Path, details: list[dict[str, Any]]) -> None:
    path.parent.mkdir(parents=True, exist_ok=True)
    fieldnames = [
        "annotation_kind",
        "annotation_mode",
        "image_stem",
        "image_name",
        "head_index",
        "detection_index",
        "head_iou",
        "gt_eye_points",
        "pred_eye_points",
        "matched_eye_points",
        "mean_pixel_error",
        "mean_normalized_error",
        "point_matches",
    ]
    with path.open("w", newline="", encoding="utf-8") as file:
        writer = csv.DictWriter(file, fieldnames=fieldnames, extrasaction="ignore")
        writer.writeheader()
        writer.writerows(details)


def main() -> int:
    args = parse_args()
    ground_truth = load_json(args.ground_truth)
    predictions = load_json(args.predictions)
    summary, details = evaluate(ground_truth, predictions, args.iou_threshold, args.comparison_scope)
    write_summary(args.summary_output, summary)
    write_details(args.details_output, details)
    print(f"summary: {args.summary_output}")
    print(f"details: {args.details_output}")
    for mode, mode_summary in summary["by_annotation_mode"].items():
        print(
            mode,
            "matched_faces:",
            mode_summary["matched_faces"],
            "eye_recall:",
            mode_summary["eye_point_recall_on_matched_faces"],
            "mean_nme:",
            mode_summary["mean_normalized_error"],
        )
    return 0


if __name__ == "__main__":
    sys.exit(main())
